def main():
    with open(FILE) as f:
        data = json.load(f)

    for entry in data:
        code = entry['code']
        if code in BATCH:
            entry['i18n'] = BATCH[code]
            # Validate counts match
            for lang in ('ru', 'ua'):
                loc = BATCH[code][lang]
                assert len(loc['benefits']) == len(entry['benefits']), f"{code}/{lang}: benefits count mismatch"
                assert len(loc['eligibility']['requirements']) == len(entry['eligibility']['requirements']), f"{code}/{lang}: requirements count mismatch"
                assert len(loc['applicationProcess']['documents']) == len(entry['applicationProcess']['documents']), f"{code}/{lang}: documents count mismatch"

    with open(FILE, 'w') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    codes = list(BATCH.keys())
    print(f"✔ Batch 1 done: added i18n to {len(codes)} countries: {', '.join(codes)}")

if __name__ == '__main__':
    main()
